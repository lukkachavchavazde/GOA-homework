// The async keyword is used to define an asynchronous function that returns a promise.

// The await keyword allows waiting for a promise to resolve before continuing execution.

// Using async/await improves readability by eliminating nested callback structures.

// The await keyword can only be used inside an async function.

// If an awaited promise is rejected, an error is thrown unless handled with try/catch or .catch().

// Multiple await statements inside an async function execute sequentially unless optimized with Promise.all().