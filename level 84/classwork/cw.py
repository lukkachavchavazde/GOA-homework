# When we raise 2 to any power, the binary representation will always be a '1' followed by the appropriate number of '0's.
# For example:
# 2^0 = 1 -> in binary: 1
# 2^1 = 2 -> in binary: 10
# 2^2 = 4 -> in binary: 100
# 2^3 = 8 -> in binary: 1000
# 2^4 = 16 -> in binary: 10000
# This is the general rule: 2^n in binary is always 1 followed by n zeros.

# When we subtract 1 from any power of 2, the result in binary will be a series of '1's.
# For example:
# 2^1 - 1 = 1 -> in binary: 1
# 2^2 - 1 = 3 -> in binary: 11
# 2^3 - 1 = 7 -> in binary: 111
# 2^4 - 1 = 15 -> in binary: 1111
# 2^5 - 1 = 31 -> in binary: 11111
# The general rule is: 2^n - 1 in binary is always n ones.
